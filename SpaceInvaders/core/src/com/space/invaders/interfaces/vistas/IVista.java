package com.space.invaders.interfaces.vistas;

/***
 * Define la interfaz para las vistas del juego.
 */
public interface IVista {
	
	/**
	 * Obtiene el ancho de la vista.
	 * @return Ancho de la vista.
	 */
	float getWidth();
	
	/**
	 * Obtiene el alto de la vista.
	 * @return Altura de la vista.
	 */
	float getHeight();
	
}